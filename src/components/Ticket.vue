      },
      taskLabel: h => {
        if (this.binding.tasking) {
          return h('div', [h('span', '历史订单'), h('spin')])
        } else {
          return h('div', [h('span', '历史订单')])
        }
      },
      binding: {
        t: undefined,
        i: undefined,
        loading: false,
        disabled: true,
        tableShow: false,
        tableHeight: '',
        submitDisabled: false,
        spinShow: true,
        submitButton: '',
        contactCommon: 0,
        tasking: false
      },
      admin: {
        admin: false,
        code: ''
      },
      fcs: [{ id: 255, jianpin: 'cds', name: '成都市', pinyin: 'chengdushi' }],
      tcs: [],
      flights: {
        columns: [{ title: 'Id', key: 'SchId' }, { title: '乘车车站', key: 'CarryStaName' }, { title: '终点站', key: 'EndStaName' }, { title: '发车时间', key: 'DrvTime' }, { title: '里程', key: 'Mile' }, { title: '车型', key: 'BusTypeName' }, { title: '票价', key: 'FullPrice' }],
        data: []
      },
      userCommon: [],
      checkGroup: [],
      relMap: {},
      relMap2: {},
      tabShow: {
        first: true,
        second: false,
        third: false
      },
      afterToday: {
        disabledDate(time) {
          return time.getTime() < Date.now() - 8.64e7
        }
      },
      formModel: {
        status: 0,
        // session: localStorage.getItem('jsessionid'),
        binding: {
          idx1: undefined,
          idx2: undefined,
          date: undefined
        },
        ticket: {
          fromCity: {},
          toCity: {},
          date: '',
          schId: ''
        },
        contact: {
          name: '',
          phone: '',
          email: ''
        },
        passengers: [
          {
            index: 0,
            name: '',
            id: ''
          }
        ],
        scheduleInfo: {}
      },
      ruleValidate: {
        'binding.idx1': [{ type: 'number', required: true, message: '请选择出发城市', trigger: 'change' }],
        'binding.idx2': [{ type: 'number', required: true, message: '请选择到达城市', trigger: 'change' }],
        'binding.date': [{ required: true, type: 'date', message: '请选择出发日期', trigger: 'change' }],
        'passengers.0': [
          {
            required: true,
            type: 'object',
            trigger: 'blur',
            fields: {
              name: [{ type: 'string', message: '请填写乘客1的姓名', required: true }, { type: 'string', min: 2, max: 10, message: '名字长度应在2-10字符之间' }],
              id: [{ type: 'string', message: '请填写乘客1的身份证号', required: true }, { pattern: /^(\d{6})(18|19|20)?(\d{2})([01]\d)([0123]\d)(\d{3})(\d|X)$/, message: '请填写正确的身份证号' }]
            }
          }
        ],
        'contact.name': [{ required: true, message: '请填写联系人姓名', trigger: 'blur' }, { type: 'string', min: 2, max: 10, message: '名字长度应在2-10字符之间', trigger: 'blur' }],
        'contact.phone': [
          { required: true, message: '请填写联系人手机号', trigger: 'blur' },
          {
            pattern: /^(13[0-9]|14[579]|15[0-3,5-9]|17[0135678]|18[0-9])\d{8}$/,
