    }
  },
  created() {
    let token = localStorage.getItem('token') || ''
    if (token !== '') {
      Util.ajax
        .get('/user/login', { params: { token: token } })
        .then(resp => {
          this.fetchData('/ticket/init', token)
        })
        .catch(error => {
          // console.log(error.response.status)
          switch (error.response.status) {
            case 400:
              break
            case 401:
              localStorage.removeItem('token')
              this.$router.replace({ path: '/' })
              break
            case 500:
              // todo
              break
          }
        })
    } else {
      this.$router.replace({ path: '/' })
    }
  },
  methods: {
    ajaxFrom(query) {
      query = query.trim()
      if (query.length > 0) {
        this.binding.loading = true
      } else {
        if (this.binding.t) {
          clearTimeout(this.binding.t)
        }
        this.fcs = []
        this.binding.loading = false
        this.clear1()
        return
      }
      if (this.binding.t) {
        clearTimeout(this.binding.t)
      }
      this.binding.t = setTimeout(() => {
        Util.ajax.get('/city', { params: { from: query } }).then(resp => {
          this.fcs = resp.data
          this.binding.loading = false
          this.tcs = []
          this.binding.t = undefined
        })
      }, 500)
    },
    ajaxTarget(query) {
      query = query.trim()
      if (query.length > 0) {
        this.binding.loading = true
      } else {
        if (this.binding.t) {
          clearTimeout(this.binding.t)
        }
        this.tcs = []
        this.binding.loading = false
        this.clear2()
        return
      }
      if (this.binding.t) {
        clearTimeout(this.binding.t)
      }
      this.binding.t = setTimeout(() => {
        let fromCity = this.formModel.ticket.fromCity
        Util.ajax.get('/city', { params: { to: query, id: fromCity.id, from: fromCity.pinyin } }).then(resp => {
          this.tcs = resp.data
          this.binding.loading = false
          this.binding.t = undefined
        })
      }, 500)
    },
    handleSelect1(val) {
      this.binding.disabled = false
      this.formModel.ticket.fromCity = this.fcs[val]
    },
    handleSelect2(val) {
      this.formModel.ticket.toCity = this.tcs[val]
      this.findFlight()
    },
    pickDate(str) {
      if (!str) {
        this.binding.tableShow = false
        this.binding.tableHeight = ''
        this.flights.data = []
        this.formModel.ticket.schId = ''
        this.formModel.scheduleInfo = {}
        return
      }
      this.formModel.ticket.date = str
      this.findFlight(str)
      // console.log(this.formModel)
    },
    findFlight(str) {
      str = str || this.formModel.ticket.date
      if (!str) {
        return
      }
      let fromCity = this.formModel.ticket.fromCity
      let toCity = this.formModel.ticket.toCity
      if (!fromCity || !toCity) {
        console.log('from city or to city is empty')
        return
      }

      let _this = this
      let prefun = function() {
        return new Promise(function(resolve, reject) {
          _this.doFindFlight(fromCity.name, fromCity.id, toCity.name, str, resolve)
        })
      }

      let nextfun = function(prevalue) {
        return new Promise(function(resolve, reject) {
          if (!prevalue) {
            let now = new Date()
            now.setDate(now.getDate() + 1)
            let date = Util.dateformat.format(now, 'yyyy-MM-dd')
            _this.doFindFlight(fromCity.name, fromCity.id, toCity.name, date, resolve)
            _this.$Message.warning({
              title: '提醒',
              content: '所查日期（' + str + '）没有班次信息，拉取明天（' + date + '）的班次信息作为参考',
              duration: 10,
              closable: true
            })
            if (_this.flights.columns.length === 8) {
              _this.flights.columns.splice(7, 1)
            }
          } else {
            if (_this.flights.columns.length === 7) {
              _this.flights.columns.push({ title: '余票', key: 'SAmount' })
            }
          }
        })
      }

      prefun()
        .then(value => {
          nextfun(value)
        })
        .catch()
    },
    doFindFlight(fname, fid, tname, date, resolve) {
      Util.ajax('/ticket/flight', { params: { to: tname, id: fid, from: fname, date: date } }).then(resp => {
        let hasResult = resp.data.Data && resp.data.Data.length > 0
        if (hasResult) {
          let data = resp.data.Data
          for (let i = 0, len = data.length; i < len; i++) {
            let x = data[i]
            if (i === 0) {
              this.formModel.ticket.schId = x.SchId
              this.formModel.scheduleInfo = x
              x._highlight = true
            }
            x.DrvTime = x.DrvDateTime.substr(11, 5)

            if (x.SchTypeName !== '固定班') {
              if (x.SchTypeName === '流水班') {
                x.DrvTime = x.DrvTime + '前'
              } else {
                x.DrvTime = x.DrvTime + x.SchTypeName.substr(0, 2)
              }
            }
          }
          this.flights.data = resp.data.Data
          this.binding.tableShow = true
          if (this.flights.data.length > 3) {
            this.binding.tableHeight = 175
          } else {
            this.binding.tableHeight = ''
          }
        }
        resolve(hasResult)
      })
    },
    clear1() {
      this.fcs = []
      this.formModel.ticket.fromCity = {}
      // clear target city
      this.formModel.binding.idx2 = ''
      this.clear2()
      this.binding.disabled = true
    },
    clear2() {
      this.tcs = []
      this.formModel.ticket.toCity = {}
      // this.formModel.ticket.date = undefined
      this.binding.tableShow = false
      this.binding.tableHeight = ''
      this.formModel.ticket.schId = ''
      this.formModel.scheduleInfo = {}
    },

    handleSubmit(name) {
      this.binding.submitDisabled = true
      let si = setInterval(() => {
        if (this.binding.submitButton === '') {
          this.binding.submitButton = 5
        } else {
          this.binding.submitButton--
          if (this.binding.submitButton <= 0) {
            this.binding.submitButton = ''
            this.binding.submitDisabled = false
            clearInterval(si)
          }
        }
      }, 1000)
      this.$refs[name].validate(valid => {
        if (valid) {
          let data = {
            task: this.formModel,
            token: localStorage.getItem('token')
          }
          Util.ajax
            .post('/ticket', data)
            .then(resp => {
              //                console.log(resp)
              let m = JSON.parse(JSON.stringify(this.formModel))
              this.tasks.unshift(m)

              this.formModel.passengers = [{ index: 0, name: '', id: '' }]
              this.relMap = {}
              this.relMap2 = {}
              for (let i in this.checkGroup) {
                this.checkGroup[i] = -Math.abs(this.checkGroup[i])
              }
            })
            .catch(error => {
              switch (error.response.status) {
                case 403:
                  this.$Message.error('非法操作!')
                  localStorage.clear()
                  this.$router.replace({ path: '/' })
                  break
                case 400:
                  this.$Message.error('请求有误，请刷新重试!')
                  break
              }
            })
          // this.$Message.success('提交成功!')
          this.$Notice.open({
            title: '提交成功',
            desc: '已成功提交订单，请前往【历史订单】查看'
          })
          // console.log(this.tasks)
        } else {
          setTimeout(() => {
            this.binding.submitDisabled = false
          }, 1500)
          this.$Message.error('表单验证失败!')
        }
      })
    },
    handleReset(name) {
      this.$refs[name].resetFields()
    },
    handleAdd() {
      let len = this.formModel.passengers.length
      if (len >= 5) {
        this.$Message.error('一次最多添加5位乘客')
        return
      }
      this.$refs['formModel'].validate(valid => {
        if (!valid) {
          this.$Message.error('请先完善信息')
        } else {
          let index = this.formModel.passengers[len - 1].index + 1
          this.formModel.passengers.push({
            index: index,
            name: '',
            id: ''
          })
          let key = 'passengers.' + index

          this.ruleValidate[key] = [
            {
              required: true,
              type: 'object',
              trigger: 'blur',
              fields: {
                name: [{ type: 'string', message: '请填写乘客' + (index + 1) + '的姓名', required: true }, { type: 'string', min: 2, max: 10, message: '名字长度应在2-10字符之间' }],
                id: [{ type: 'string', message: '请填写乘客' + (index + 1) + '的身份证号', required: true }, { pattern: /^(\d{6})(18|19|20)?(\d{2})([01]\d)([0123]\d)(\d{3})(\d|X)$/, message: '请填写正确的身份证号' }]
              }
            }
          ]
        }
      })
    },
    handleRemove(index) {
      if (this.formModel.passengers.length === 1) {
        this.$Message.error('至少需要一个乘客')
        return
      }
      // console.log('remove before', this.relMap)
      let p = this.formModel.passengers[index]
      this.formModel.passengers.splice(index, 1)
      let ri = this.relMap2[p.index]
      if (ri !== undefined) {
        this.checkGroup[ri] = 0 - this.checkGroup[ri]
        delete this.relMap[ri]
        delete this.relMap2[p.index]
      }
      // console.log('remove after', this.relMap)
    },
    tableSelect(currentRow, oldRow) {
      this.formModel.ticket.schId = currentRow.SchId
      this.formModel.scheduleInfo = currentRow
      // console.log(this.formModel)
    },
    handleTabPane(name) {
      for (let k in this.tabShow) {
        if (name === k) {
          this.tabShow[k] = true
        } else {
          this.tabShow[k] = false
        }
      }
      // console.log(name, this.tabShow)
    },
    handleCancel(index, id) {
      // console.log(id)
      Util.ajax
        .post('/ticket/' + id, {})
        .then(resp => {
          // console.log(resp.data)
          this.tasks[index].status = 2
        })
        .catch(error => {
          console.log(error)
        })
    },
    handleDelete(index, id) {
      // console.log(index, id)
      Util.ajax
        .delete('/ticket/' + id)
        .then(resp => {
          // console.log(resp.data)
          this.tasks.splice(index, 1)
        })
        .catch(error => {
          console.log(error)
        })
    },
    handleCheck(index) {
      // console.log(index)
      if (index > 0) {
        let len = this.formModel.passengers.length
        if (len >= 5) {
          this.$Message.error('一次最多添加5位乘客')
          return
        }
        let i = index - 1
        let p = JSON.parse(JSON.stringify(this.userCommon[i]))
        // let id = p.id + ''
        let pi
        let lastPassenger = this.formModel.passengers[len - 1]
        if (!lastPassenger.name) {
          pi = lastPassenger.index
          p.index = lastPassenger.index
          this.formModel.passengers[len - 1] = p
        } else {
          pi = lastPassenger.index + 1
          p.index = pi
          this.formModel.passengers.push(p)
        }
        this.relMap[i] = pi
        this.relMap2[pi] = i
        // console.log('check check', this.relMap, this.formModel.passengers)
      } else {
        let i = 0 - index - 1
        let pi = this.relMap[i]
        let len = this.formModel.passengers.length
        for (let j in this.formModel.passengers) {
          if (pi === this.formModel.passengers[j].index) {
            if (len === 1) {
              this.formModel.passengers[0].index = 0
              this.formModel.passengers[0].name = ''
              this.formModel.passengers[0].id = ''
              delete this.formModel.passengers[0].objectId
            } else {
              this.formModel.passengers.splice(j, 1)
            }
            delete this.relMap[i]
            delete this.relMap2[pi]
          }
        }
        // console.log('check uncheck', this.relMap, this.formModel.passengers)
      }
    },
    logout() {
      // console.log('click logout')
      localStorage.removeItem('token')
      this.$router.replace({ path: '/' })
    },
    handleRadio(index) {
      // console.log(index, this.binding.contactCommon)
      this.formModel.contact = JSON.parse(JSON.stringify(this.contacts[index]))
    },
    handleContactChange(event) {
      // console.log(event)
      this.binding.contactCommon = -1
    },
    refresh() {
      this.binding.tasking = false
      clearInterval(this.binding.i)
      let token = localStorage.getItem('token') || ''
      if (token === '') {
        this.$router.replace({ path: '/' })
        return
      }
      this.binding.spinShow = true
      this.fetchData('/ticket/refresh', token)
    },
    fetchData(url, token) {
      let out = setTimeout(() => {
        this.binding.spinShow = false
      }, 5000)
      Util.ajax
        .get(url, { params: { token: token } })
        .then(resp => {
          let ret = resp.data
          // console.log('ticket init', ret)
          this.binding.tasking = ret.tasking
          if (ret.tasks && ret.tasks.length > 0) {
            this.tasks = ret.tasks
          }
          if (ret.pending && ret.pending.length > 0) {
            this.$Notice.open({
              title: '有待支付订单',
              desc: '您有订单尚未支付，请前往【待支付订单】查看详情'
            })
            this.pending = ret.pending
            this.binding.i = setInterval(() => {
              for (let i in this.pending) {
                if (this.pending[i].payTime <= 0) {
                  delete this.pending[i]
                } else {
                  this.pending[i].payTime -= 1
                }
              }
              if (this.pending.length === 0) {
                clearInterval(this.pending)
              }
            }, 60 * 1000)
          }
          if (ret.contact && ret.contact.length > 0) {
            this.formModel.contact = ret.contact[0]
            if (ret.contact.length > 1) {
              this.contacts = JSON.parse(JSON.stringify(ret.contact))
            }
          }
          if (ret.passengers && ret.passengers.length > 0) {
            this.userCommon = ret.passengers
          }
          if (ret.admin) {
            this.admin.admin = true
            if (ret.code) {
              this.admin.code = ret.code.code
            }
          }
          this.binding.spinShow = false
          clearTimeout(out)
        })
        .catch(error => {
          console.log(error)
        })
    },
    genCode() {
      Util.ajax.post('/admin/invitation', { token: localStorage.getItem('token'), count: 10 }).then(resp => {})
    },
    handleModify(index) {
      let ri = this.relMap2[index]
      if (ri !== undefined) {
        this.checkGroup[ri] = this.checkGroup[ri] * -1
        delete this.relMap[ri]
        delete this.relMap2[ri]
        if (index === 0) {
          this.checkGroup = JSON.parse(JSON.stringify(this.checkGroup))
        }
      }
    }
  },
  destroyed() {
    clearTimeout(this.binding.t)
  }
}
